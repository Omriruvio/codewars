function myFirstInterpreter(code) {
  code = code.replace(/[^+\.]/g, '');
  let cell = 0;
  let result = '';
  for (const x of [...code]) {
    x === '+' ? ++cell : (result += String.fromCharCode(cell));
    cell = cell % 256;
  }
  return result;
}
